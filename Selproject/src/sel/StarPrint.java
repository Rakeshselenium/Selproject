package sel;

public class StarPrint {

			public static void main(String[] args) {
				int i;
				int s=1, line=4;
				for (i=1;i<=line;i++)
				{
					for (int j=1;j<=s;j++)
					{
						
					System.out.print(""+j);
					}
					System.out.println(" ");
					s++;
					
				}

			}
}

		



